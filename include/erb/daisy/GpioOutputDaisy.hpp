/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : ctor
==============================================================================
*/

GpioOutputDaisy::GpioOutputDaisy (const dsy_gpio_pin & pin)
:  _impl ({pin, DSY_GPIO_MODE_OUTPUT_PP, DSY_GPIO_NOPULL})
{
   dsy_gpio_init (&_impl);
}



/*
==============================================================================
Name : write
==============================================================================
*/

void  GpioOutputDaisy::write (bool val)
{
   dsy_gpio_write (&_impl, val ? 1 : 0);
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : impl_deinit
Description :
   Hack for NPR deactivation. Do not use pin after this call.
==============================================================================
*/

void  GpioOutputDaisy::impl_deinit ()
{
   dsy_gpio_deinit (&_impl);
}



