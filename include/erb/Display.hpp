/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : ctor
==============================================================================
*/

Display::Display (Storage & data)
:  impl_data (data)
{
}



/*
==============================================================================
Name : operator =
==============================================================================
*/

Display &   Display::operator = (const Storage & data)
{
   impl_data = data;

   return *this;
}



/*
==============================================================================
Name : operator Storage &
==============================================================================
*/

Display::operator Storage & ()
{
   return impl_data;
}



/*
==============================================================================
Name : fill
==============================================================================
*/

void  Display::fill (bool val)
{
   impl_data.fill (val ? 0xff : 0x00);
}



